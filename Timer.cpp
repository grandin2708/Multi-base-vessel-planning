//--------------------------------------------------------------------
Timer::Timer()
//--------------------------------------------------------------------
{
  started_ = false;
  stopped_ = false;
}

//--------------------------------------------------------------------
Timer::~Timer()
//--------------------------------------------------------------------
{

}

//--------------------------------------------------------------------
void Timer::startTimer()
//--------------------------------------------------------------------
{
  start_ = clock();
  started_ = true;
  stopped_ = false;
}

//--------------------------------------------------------------------
void Timer::stopTimer()
//--------------------------------------------------------------------
{
  stopped_ = true;
  stop_ = clock();
}

//--------------------------------------------------------------------
double Timer::continueTimer()
//--------------------------------------------------------------------
{
  clock_t diff = clock()-stop_;
  start_ += diff;
  limit_ += diff;
  started_ = true;
  stopped_ = false;
  return (double)diff/CLOCKS_PER_SEC;
}

//--------------------------------------------------------------------
double  Timer::getElapsedSeconds()
//--------------------------------------------------------------------
{
  double ret;
  if (stopped_)
    ret = (double)(stop_-start_)/CLOCKS_PER_SEC; 
  else
    ret = (double)(clock()-start_)/CLOCKS_PER_SEC; 

  return ret;
}

//--------------------------------------------------------------------
void Timer::setTimeLimit(int seconds)
//--------------------------------------------------------------------
{
  limit_ = clock() + seconds*CLOCKS_PER_SEC;
}

//--------------------------------------------------------------------
bool Timer::hasPassedLimit()
//--------------------------------------------------------------------
{
  return ( limit_ < clock() );
}

